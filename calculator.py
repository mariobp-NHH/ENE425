
def operation(num1,num2,oper_name):
    num1=float(num1)
    num2=float(num2)
    if oper_name == 'add':
        oper_result = num1 + num2
    elif operation == 'subtract':
        oper_result = num1 - num2
    elif operation == 'multiply':
        oper_result = num1 * num2
    elif operation == 'divide':
        oper_result = num1 / num2

    return oper_result